

class Vozilo {
    private int brzina;
    private int kapacitetGoriva;

   
    public Vozilo(int brzina, int kapacitetGoriva) {
        this.brzina = brzina;
        this.kapacitetGoriva = kapacitetGoriva;
    }

  
    public int getBrzina() {
        return brzina;
    }

    public int getKapacitetGoriva() {
        return kapacitetGoriva;
    }
}



